    dense = dense or 0
    local _l_dense = math.max(0, dense*10-math.floor(math.random()*1.5))
    _l_dense = math.floor(_l_dense)

    local rnd = (math.floor(math.random()* #_l_pattern[_l_dense])+1) 
    return _l_pattern[_l_dense][rnd]
end
